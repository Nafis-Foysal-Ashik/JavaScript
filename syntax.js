/*
//Variable nameing system:
JavaScript also treats a dollar sign as a letter.
Below are the valid variable naming convention:
let $ = "Hello World"; ->ok
let $$$ = 2; ->ok
let $myMoney = 5; -> ok

JavaScript treats underscore as a letter.
Below are the valid variable naming convention:
let _lastName = "Johnson"; ->ok
let _x = 2; ->ok
let _100 = 5; ->ok

*/

